-- ── ANALYTICS VIEWS ─────────────────────────────────────────────

CREATE VIEW v_sales_summary AS
SELECT
    s.sales_id,
    s.invoice_datetime,
    s.customer_name,
    s.salesperson,
    s.invoice_total,
    s.total_items_count,
    COUNT(DISTINCT si.item_id)            AS unique_products,
    STRING_AGG(DISTINCT sp.account, ', ') AS payment_methods
FROM sales s
LEFT JOIN sale_items    si ON si.sales_id = s.sales_id
LEFT JOIN sale_payments sp ON sp.sales_id = s.sales_id
GROUP BY s.sales_id;


CREATE VIEW v_revenue_by_day AS
SELECT
    DATE(invoice_datetime)  AS sale_date,
    COUNT(*)                AS total_sales,
    SUM(invoice_total)      AS total_revenue
FROM sales
GROUP BY DATE(invoice_datetime)
ORDER BY sale_date;


CREATE VIEW v_top_products AS
SELECT
    p.item_id,
    p.name,
    p.category,
    p.selling_price,
    p.cost_price,
    (p.selling_price - p.cost_price)  AS margin,
    SUM(si.qty)                        AS total_qty_sold,
    SUM(si.total)                      AS total_revenue
FROM sale_items si
JOIN products p ON p.item_id = si.item_id
GROUP BY p.item_id, p.name, p.category, p.selling_price, p.cost_price
ORDER BY total_revenue DESC;


CREATE VIEW v_payment_breakdown AS
SELECT
    account,
    COUNT(DISTINCT sales_id)  AS num_transactions,
    SUM(amount)               AS total_amount
FROM sale_payments
GROUP BY account
ORDER BY total_amount DESC;


CREATE VIEW v_supplier_products AS
SELECT
    s.person_id    AS supplier_id,
    s.company_name AS supplier,
    p.item_id,
    p.name         AS product_name,
    p.category,
    p.cost_price,
    p.selling_price,
    p.quantity
FROM product_suppliers ps
JOIN suppliers s ON s.person_id = ps.supplier_id
JOIN products  p ON p.item_id   = ps.item_id
ORDER BY s.company_name, p.name;


CREATE VIEW v_customer_balance AS
SELECT
    person_id,
    name,
    phone,
    balance,
    credit_limit,
    points
FROM customers
WHERE balance > 0
ORDER BY balance DESC;