-- ── SALES ───────────────────────────────────────────────────────
CREATE TABLE sales (
    sales_id           INTEGER PRIMARY KEY,
    invoice_total      NUMERIC(12,2) NOT NULL,
    customer_id        INTEGER REFERENCES customers(person_id),
    customer_name      TEXT,
    salesperson        TEXT,
    invoice_policy     TEXT,
    invoice_datetime   TIMESTAMPTZ NOT NULL,
    total_items_count  INTEGER DEFAULT 0,
    items_count        INTEGER DEFAULT 0,
    scraped_at         TIMESTAMPTZ DEFAULT NOW()
);

-- ── SALE_ITEMS ───────────────────────────────────────────────────
CREATE TABLE sale_items (
    id          BIGSERIAL PRIMARY KEY,
    sales_id    INTEGER NOT NULL REFERENCES sales(sales_id) ON DELETE CASCADE,
    item_id     INTEGER REFERENCES products(item_id),
    name        TEXT NOT NULL,
    qty         INTEGER NOT NULL DEFAULT 1,
    unit_price  NUMERIC(12,2) NOT NULL,
    total       NUMERIC(12,2) NOT NULL
);

-- ── SALE_PAYMENTS ────────────────────────────────────────────────
CREATE TABLE sale_payments (
    id                  BIGSERIAL PRIMARY KEY,
    sales_id            INTEGER NOT NULL REFERENCES sales(sales_id) ON DELETE CASCADE,
    account             TEXT NOT NULL,
    account_id          INTEGER,
    amount              NUMERIC(12,2) NOT NULL,
    transaction_medium  TEXT
);

